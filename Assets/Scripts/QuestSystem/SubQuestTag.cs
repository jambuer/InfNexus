/// <summary>
/// Görevlerin ikincil kategorilerini/türlerini tanımlar.
/// </summary>
public enum SubQuestTag
{
    None,           // Herhangi bir alt kategoriye ait değil
    Social,         // Sosyal etkileşim veya ilişki görevleri
    Fight,          // Savaş veya mücadele görevleri
    Gathering,      // Toplama veya hasat görevleri
    Production,     // Üretim veya zanaat görevleri
    Battlefield,    // Savaş alanı temalı görevler
    Clan,           // Klan veya grup görevleri
    HideSeeking,    // Saklambaç veya keşif görevleri
    Explorer,       // Keşif veya harita açma görevleri
    Crafting,       // Zanaat veya eşya yapım görevleri
    Emporium        // Ticaret veya pazar yeri görevleri
}