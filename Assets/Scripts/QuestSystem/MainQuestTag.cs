/// <summary>
/// Görevlerin birincil kategorilerini tanımlar.
/// </summary>
public enum MainQuestTag
{
    None,           // Herhangi bir ana kategoriye ait değil
    Quest,          // Genel bir görev
    Allied,         // Müttefiklerle ilgili görevler
    Nexus,          // Nexus şehri veya varlığı ile ilgili görevler
    Character,      // Belirli bir karakterle ilgili görevler
    Spirit,         // Ruhani veya mistik görevler
    Special,        // Özel etkinlik veya benzersiz görevler
    Challenge,      // Zorlu mücadele görevleri
    Guild,          // Lonca görevleri
    Race,           // Irk temalı görevler
    Region,         // Bölgeye özel görevler
    Area,           // Belli bir alana özel görevler
    Continent,      // Kıta genelindeki görevler
    World           // Tüm dünyayı etkileyen görevler
}